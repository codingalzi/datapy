# ### 람다 함수

# 람다 함수는 이름 없이 정의된 함수를 의미한다. 
# k개의 인자를 받는 함수를 람다 함수로 정의하는 양식은 아래와 같다. 
# 
# ```python
# lambda 변수1, 변수2, ..., 변수k: 반환값
# ```
# 
# __주의사항:__ 반환값을 지정할 때 `return` 예약어가 사용되지 않는다.

# #### 예제
# 
# 예를 들어 두 수의 합을 반환하는 함수는 다음과 같이 정의한다.

# In[38]:


