# 아래 함수는 임의의 리스트에 대해 앞서 언급된 전처리를 수행하는 함수이다.

# In[20]:


def clean_strings(strings):
    result = []
    for value in strings:
        value = value.strip()
        value = re.sub('[!#?]', '', value)
        value = value.title()
        result.append(value)
    return result


