# ## `None` 값

# `None`은 어떤 의미도 없는 값, 소위 널(null)값이며, 
# 문법적으로 `NoneType` 자료형의 유일한 값이다. 

# In[44]:


type(None)


# In[45]:


a = None
a is None


# In[46]:


